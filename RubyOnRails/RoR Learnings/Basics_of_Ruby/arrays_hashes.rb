# Arrays
numbers = [1, 2, 3, 4, 5]
fruits = ["apple", "banana", "orange"]

puts "First number: #{numbers.first}"
puts "Last fruit: #{fruits[1]}"
puts "Last fruit: #{fruits.last}"

# Hashes
person = {
  "name" => "John",
  "age" => 30,
  "city" => "New York"
}

puts "Name: #{person["name"]}"
puts "Age: #{person["age"]}"
puts "City: #{person["city"]}"
