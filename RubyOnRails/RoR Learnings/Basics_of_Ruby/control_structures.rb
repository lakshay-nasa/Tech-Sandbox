# if-else statement
x = 10
if x > 5
  puts "x is greater than 5"
else
  puts "x is less than or equal to 5"
end

# unless statement
y = 3
unless y > 5
  puts "y is not greater than 5"
end

# case statement
grade = "B"
case grade
when "A"
  puts "Excellent!"
when "B"
  puts "Good job!"
when "C"
  puts "Needs improvement"
else
  puts "Failed"
end
